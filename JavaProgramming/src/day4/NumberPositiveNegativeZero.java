package day4;

public class NumberPositiveNegativeZero {

	public static void main(String[] args) {

		int number = 136548;

		if (number > 0) {
			System.out.println("Number positive: " + number);
		} else if (number == 0) {
			System.out.println("Number Zero: " + number);
		} else {
			System.out.println("Negative number: " + number);
		}

	}

}
