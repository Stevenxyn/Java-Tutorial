package day3;

public class AssigmentOperators {

	public static void main(String[] args) {

		// Assigment Operators

		// Example #1 - Sum
		int a = 10;

		// a = a + 5;
		a += 5;

		System.out.println(a);
		System.out.println("***********************************");

		// Example #2 - subtract
		int b = 15;

		// b = b - 5;
		b -= 5;

		System.out.println(b);
		System.out.println("***********************************");

		// Example #3 - Division
		int s = 26;

		// s = s / 5;
		s /= 2;

		System.out.println(s);
		System.out.println("***********************************");

		
		// Example #4 - Multiplicacion
		int m = 10;

		// m = m * 5;
		m *= 2;

		System.out.println(m);
		System.out.println("***********************************");
		
		// Example #3 - resto
		int h = 26;

		// h = h % 5;
		h %= 3;

		System.out.println(h);
		System.out.println("***********************************");
	}

}
