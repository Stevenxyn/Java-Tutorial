
public class OperadoresDeComparacion {

	public static void main(String[] args) {
		double salary = 1800.524;
		double salary2 = 1800.521;
		
		//Operadores de comparacion
		System.out.println(salary > salary2);
		System.out.println(salary < salary2);
		System.out.println(salary >= salary2);
		System.out.println(salary <= salary2);
		System.out.println(salary != salary2);
		System.out.println(salary == salary2);
		
	}

}
