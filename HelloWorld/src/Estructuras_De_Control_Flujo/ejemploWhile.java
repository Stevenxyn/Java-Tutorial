package Estructuras_De_Control_Flujo;

public class ejemploWhile {

	public static void main(String args[]) {
		
		//while "mientras" se cumpla la condicion se ejecutara el bloque siguiente de codigo
		
		int fecha = 1999;
		int contador = 0;
		
		while(fecha <= 2024) {
			System.out.println("Pasaron " + contador + " años del " + fecha + " hasta " + 2024);
			contador++;
			fecha++;
			
		}
	
		
		
		
		
	}
}
